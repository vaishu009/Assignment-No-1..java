/*
Program to Check Whether Number is Even or Odd
 */
public class EvenOdd {
    public static void main(String args[])
    {
        int num=11;
        
        if(num%2==0)
        {
            System.out.println("Number is even");
        }
        else
        {
            System.out.println("Number is Odd");
        }
    }
}
